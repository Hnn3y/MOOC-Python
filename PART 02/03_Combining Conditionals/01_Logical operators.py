#You can combine conditions with the logical operators 'and' and 'or'. 
# The operator 'and' specifies that all the given conditions must be true at the same time. 
# The operator 'or' specifies that at least one of the given conditions must be true.

# The following truth table contains the behaviour of these operators in different situations:

#a	       b	     a and b	a or b
#False	False	  False	   False
#True	   False	  False	   True
#False	True	  False	   True
#True	   True	  True	   True 

