# A statement is a part of the program which executes something. 
# It often, but not always, refers to a single command.
#For example, print("Hi!") is a statement which prints out a line of text. 
# Likewise, number = 2 is a statement which assigns a value to a variable.
#A statement can also be more complicated. 
# It can, for instance, contain other statements. 
# The following statement spans three lines:

if name == "Anna":
    print("Hi!")
    number = 2
    
#In the above case there are two statements (a print statement and an assignment statement) within a conditional statement.